// This example blinks leds connected to pins P4, P5, P6, P7 of PCF8574T. Some
// button can be connected to other pins and its state observed on SWO.
package main

import (
	"fmt"

	"stm32/hal/gpio"
	"stm32/hal/irq"
	"stm32/hal/system"
	"stm32/hal/system/timer/rtc"

	"stm32/hal/i2c"
)

var (
	leds *gpio.Port
	twi  = &i2c.Driver{Periph: i2c.I2C2}
)

const (
	LED1 = gpio.Pin7
	LED2 = gpio.Pin6
)

func init() {
	system.Setup(8, 72/8, false)
	rtc.Setup(32768)

	gpio.B.EnableClock(true)
	leds = gpio.B
	port, pins := gpio.B, gpio.Pin10|gpio.Pin11

	cfg := gpio.Config{Mode: gpio.Out, Speed: gpio.Low}
	leds.Setup(LED1|LED2, &cfg)

	cfg = gpio.Config{
		Mode:   gpio.Alt,
		Driver: gpio.OpenDrain,
	}
	port.Setup(pins, &cfg)
	twi.EnableClock(true)
}

func i2cConfigure() {
	twi.Reset() // Mandatory!
	twi.Setup(&i2c.Config{Speed: 5000})
	twi.SetIntMode(irq.I2C2_EV, irq.I2C2_ER)
	twi.Enable()
}

func checkI2CErr(err error) bool {
	if err == nil {
		return true
	}
	if err.(i2c.Error)&i2c.SoftTimeout != 0 {
		fmt.Printf("SoftTimeout\n")
		i2cConfigure()
		return false
	}
	fmt.Printf("Error: 0x%02x\n", err)
	twi.SoftReset()
	return false
}

func main() {
	i2cConfigure()
	c := twi.MasterConn(0x27)

	out := []byte{
		0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef,
		0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef,
		0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf,
		0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf,
		0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf,
		0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf,
		0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f,
		0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f,
		0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f,
		0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f, 0x7f,
		0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf,
		0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf, 0xbf,
		0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf,
		0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf, 0xdf,
		0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef,
		0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef, 0xef,
	}
	var led bool
	for {
		if led {
			leds.SetPins(LED1)
			led = false
		} else {
			leds.ClearPins(LED1)
			led = true
		}
		_, err := c.Write(out)
		if !checkI2CErr(err) {
			continue
		}
		var in [4]byte
		n, err := c.Read(in[:])
		fmt.Printf("%2x\n", in[:n])
		if !checkI2CErr(err) {
			continue
		}
		c.SetStopRead()
		n, err = c.Read(in[:2])
		fmt.Printf("%2x\n", in[:n])
		if !checkI2CErr(err) {
			continue
		}
	}
}

func twiISR() {
	twi.ISR()
}

//emgo:const
//c:__attribute__((section(".ISRs")))
var ISRs = [...]func(){
	irq.RTCAlarm: rtc.ISR,
	irq.I2C2_EV:  twiISR,
	irq.I2C2_ER:  twiISR,
}
